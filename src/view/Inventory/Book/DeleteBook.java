    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        try {
            dtm1=BookController.getAllBookList_2();
            String itemcode1=String.valueOf(dtm1.getValueAt(infoTable2.getSelectedRow(), 0));
            
            String itemcode2=ItemCodeText.getText();

            if(itemcode2!=null){
                try {
                    ArrayList<String> itemList=BookController.SearchItem(itemcode2);
                    
                    NameText.setText(itemList.get(0));
                    LocationText.setText(itemList.get(1));
                    QtyText.setText(itemList.get(2));
                    PriceText.setText(itemList.get(3));
                    UserText.setText(itemList.get(4));
                    AuthorText.setText(itemList.get(5));
                    PublisherText.setText(itemList.get(6));
                    SellerNameText.setText(itemList.get(7));
                    ReturnDateText.setText(itemList.get(8));
                    BookCategoryText.setText(itemList.get(9));
                    StreamText.setText(itemList.get(10));
                    
                    
                } catch (HibernateException ex) {
                    Logger.getLogger(DeleteBook.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else if(itemcode1!=null){
                ArrayList<String> itemList=BookController.SearchItem(itemcode2);
                
                NameText.setText(itemList.get(0));
                LocationText.setText(itemList.get(1));
                QtyText.setText(itemList.get(2));
                PriceText.setText(itemList.get(3));
                UserText.setText(itemList.get(4));
                AuthorText.setText(itemList.get(5));
                PublisherText.setText(itemList.get(6));
                SellerNameText.setText(itemList.get(7));
                ReturnDateText.setText(itemList.get(8));
                BookCategoryText.setText(itemList.get(9));
                StreamText.setText(itemList.get(10));
            }else{
                JOptionPane.showMessageDialog(DeleteBook.this,"AnError occured", "Information", 1);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DeleteBook.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HibernateException ex) {
            Logger.getLogger(DeleteBook.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_searchButtonActionPerformed

