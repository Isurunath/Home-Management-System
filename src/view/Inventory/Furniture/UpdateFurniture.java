    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        int respond= JOptionPane.showConfirmDialog(UpdateFurniture.this,"Do you want to delete this record.? If yes click Yes", "Confimation message",0,3);

        if(respond==JOptionPane.OK_OPTION){
            try {
                String itemCode=ItemCodeText.getText();
                String name=NameText.getText();
                int qty=Integer.parseInt(QtyText.getText());
                String location=LocationText.getText();
                String seller=SellerNameText.getText();
                int warrantyPeriod=Integer.parseInt(WarrantyPeriodText.getText());
                String user=UserText.getText();
                double price=Double.parseDouble(PriceText.getText());
                String purDate=String.valueOf(PurDateCombo.getSelectedItem());
                String paymentMethod=PaymentMethodText.getText();
                int paymentPeriod=Integer.parseInt(PaymentPeriodText.getText());
                String nameOfTheSeller=SellerNameText.getText();

                Furniture furniture=new Furniture(itemCode, name, location, qty, price, user,purDate, paymentMethod, warrantyPeriod, nameOfTheSeller, paymentPeriod);
                boolean res=FurnitureController.EditFurnitureDetails(furniture);

                if(res==true){
                    try {
                        JOptionPane.showMessageDialog(UpdateFurniture.this,"Selected Record updated", "Information", 1);

                        dtm1.setRowCount(0);
                        infoTable3.setModel(FurnitureController.getAllFurnitureList_2());
                    } catch (HibernateException ex) {
                        Logger.getLogger(UpdateFurniture.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }else{
                    JOptionPane.showMessageDialog(UpdateFurniture.this,"An Error Occured", "Information", 1);
                }
            } catch (HibernateException ex) {
                Logger.getLogger(UpdateFurniture.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(UpdateFurniture.this,"User cancel the process", "Information", 1);
        }

        ItemCodeText.setText("");
        NameText.setText("");
        QtyText.setText("");
        LocationText.setText("");
        SellerNameText.setText("");
        WarrantyPeriodText.setText("");
        UserText.setText("");
        PriceText.setText("");
        PaymentMethodText.setText("");
        PaymentPeriodText.setText("");
        SimpleDateFormat date=new SimpleDateFormat("EEEE,MMMM dd,yyyy");
        java.util.Date today=Calendar.getInstance().getTime();
        PurDateCombo.setDate(today);

    }//GEN-LAST:event_updateButtonActionPerformed

