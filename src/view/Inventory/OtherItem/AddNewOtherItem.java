    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
             String itemCode=ItemCodeText.getText();
             String name=NameText.getText();
             String location=String.valueOf( LocationComboBox.getSelectedItem());
             int qty=Integer.parseInt(QtyText.getText());
             String user=String.valueOf(UserComboBox.getSelectedItem());
             String date=String.valueOf(DateCombo.getSelectedItem());
             double price=Integer.parseInt(PriceText.getText());
             String usage=UsageText.getText();
             String Description=DescriptionText.getText();
             
             Otheritem otherItem=new Otheritem(itemCode, name, location, qty, price, user, date, Description, usage);
             boolean res=OtherItemController.addOtherItem(otherItem);
             
             if(res==true){
                 JOptionPane.showMessageDialog(AddNewOtherItem.this,"One record added", "Information", 1);
                 
                 infoTable1.setModel(OtherItemController.getOtherItemList_2());
                 
             }else{
                 JOptionPane.showMessageDialog(AddNewOtherItem.this,"Unsuccessful", "Information", 1);
             }
             
             ItemCodeText.setText("");
             QtyText.setText("");
             NameText.setText("");
             PriceText.setText("");
             DescriptionText.setText("");
             UsageText.setText("");
         }  catch (HibernateException ex) {
             Logger.getLogger(AddNewOtherItem.class.getName()).log(Level.SEVERE, null, ex);
         }
            
    }//GEN-LAST:event_jButton1ActionPerformed

