    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        try {
            int respond=JOptionPane.showConfirmDialog(DeleteTool.this, "Do you want to delete this record.?", "Confirmation Message", 0, 3);

            if(respond==JOptionPane.OK_OPTION){
                String itemCode=ItemCodeText.getText();
                int res=MedicineController.deleteMedicine(itemCode);

            if(res>0){
                JOptionPane.showMessageDialog(DeleteTool.this,"Record deleted.","Information Message",1);
                
                dtm1.setRowCount(0);       
                infoTable2.setModel(ToolController.getAllToolList_1());
                
            }else{
                JOptionPane.showMessageDialog(DeleteTool.this,"Deleting process unsuccessful.","Information Message",1);
            }
            
            }else{
                JOptionPane.showMessageDialog(DeleteTool.this,"User cancel the deleting process", "Information", 1);
            }
                    ItemCodeText.setText("");
                    NameText.setText("");
                    LocationText.setText("");
                    QtyText.setText("");
                    PriceText.setText("");
                    UserText.setText("");
                    DateText.setText("");
                    SellerText.setText("");
                    WarrantyPeriodText.setText("");
                    
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DeleteTool.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HibernateException ex) {
            Logger.getLogger(DeleteTool.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_deleteButtonActionPerformed

