    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int respond= JOptionPane.showConfirmDialog(DeleteRepair.this,"Do you want to delete this record.? If yes click Yes", "Confimation message",0,3);
        
        if(respond==JOptionPane.OK_OPTION){
            try {
                String itemCode=String.valueOf(ItemCodeComboBox.getSelectedItem());
                int res=RepairController.deleteRepair(itemCode);
                
                if(res>0){
                    JOptionPane.showMessageDialog(DeleteRepair.this,"Selected record deleted", "Information", 1);
                    
                    dtm1.setRowCount(0);
                    infoTable1.setModel(RepairController.getAllRepairList_2());
                }
            }catch (HibernateException ex) {
                Logger.getLogger(DeleteRepair.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
          JOptionPane.showMessageDialog(DeleteRepair.this,"User cancel the process", "Information", 1);  
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

