    private void infoTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_infoTable1MouseClicked
        ReturnDateCombo.setEditable(true);
        RepairingDateCombo.setEditable(true);
        
        NameText.setText(String.valueOf(dtm1.getValueAt(infoTable1.getSelectedRow(), 0)));
        DescriptionText.setText(String.valueOf(dtm1.getValueAt(infoTable1.getSelectedRow(),5)));
        ReturnDateCombo.setSelectedItem(dtm1.getValueAt(infoTable1.getSelectedRow(),7));
        RepairingDateCombo.setSelectedItem(dtm1.getValueAt(infoTable1.getSelectedRow(),3));
        PriceText.setText(String.valueOf(dtm1.getValueAt(infoTable1.getSelectedRow(),4)));
        TechnicianNameText.setText(String.valueOf(dtm1.getValueAt(infoTable1.getSelectedRow(),2)));
        
    }//GEN-LAST:event_infoTable1MouseClicked

    private void TechnicianNameTextPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_TechnicianNameTextPropertyChange
        try {
            String id=String.valueOf(dtm1.getValueAt(infoTable1.getSelectedRow(),2));
            
            ArrayList<String> technicianDetails=TechnicianController.SearchTechnician(id);
            
            TechnicianIDText.setText(technicianDetails.get(0));
            AddressText.setText(technicianDetails.get(1));
            mobileText.setText(technicianDetails.get(2));
            HomeText.setText(technicianDetails.get(3));
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DeleteRepair.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HibernateException ex) {
            Logger.getLogger(DeleteRepair.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TechnicianNameTextPropertyChange


