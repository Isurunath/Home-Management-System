    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        int respond= JOptionPane.showConfirmDialog(UpdateRepair.this,"Do you want to Update this record.? If yes click Yes", "Confimation message",0,3);

        if(respond==JOptionPane.OK_OPTION){
            try {
                String itemCode=ItemCodeText.getText();
                String name=ItemCodeText.getText();
                Double price=Double.parseDouble(PriceText.getText());
                String description=DescriptionText.getText();
                String repairingDate=String.valueOf(RepairingDateCombo.getSelectedItem());
                String returnDate=String.valueOf(ReturnDateCombo.getSelectedItem());
                String category=String.valueOf(CategoryComboBox.getSelectedItem());
                
                String techName=String.valueOf(TechNameComboBox.getSelectedItem());
                String techId=TechnicianIDText.getText();
                String techAddress=AddressText.getText();
                Long mobNo=Long.parseLong(mobileText.getText());
                Long homeNo=Long.parseLong(HomeText.getText());

                Repair updateList=new Repair(techId, itemCode, name, techId, techName, repairingDate, price, description, category, returnDate);
                Technician updatetech1=new Technician(techId, name, techAddress, mobNo, homeNo);

                boolean res1=RepairController.EditRepairDetails(updateList);
                boolean res2=TechnicianController.EditTechnicianDetails(updatetech1);

                if(res1==true && res2==true){
                    dtm1.setRowCount(0);

                    infoTable1.setModel(RepairController.getAllRepairList_2());

                    JOptionPane.showMessageDialog(UpdateRepair.this,"Selected Record updated", "Information", 1);

                }else{
                    JOptionPane.showMessageDialog(UpdateRepair.this,"Updating Process unsuccessful", "Information", 1);
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(UpdateMedicine.class.getName()).log(Level.SEVERE, null, ex);
            } catch (HibernateException ex) {
                Logger.getLogger(UpdateMedicine.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        ItemCodeText.setText("");
        TechnicianIDText.setText("");
        AddressText.setText("");
        mobileText.setText("");
        PriceText.setText(" ");
        DescriptionText.setText("");
        HomeText.setText("");
        NameText.setText("");
        DescriptionText.setText("");
        
        SimpleDateFormat date=new SimpleDateFormat("EEEE,MMMM dd,yyyy");
        java.util.Date today=Calendar.getInstance().getTime();
        ReturnDateCombo.setDate(today);
        RepairingDateCombo.setDate(today);

    }//GEN-LAST:event_updateButtonActionPerformed

