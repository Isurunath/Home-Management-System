    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         int response= JOptionPane.showConfirmDialog(AddNewRepair.this,"Do you want to add a new record.? If yes click Yes", "Confimation message",0,3);
         if(response==JOptionPane.OK_OPTION){
            if("New Technician".equals(respond)){
            try {
            String techID=TechnicianIDText.getText();
            String techName=String.valueOf(TechNameComboBox.getSelectedItem());
            String address=AddressText.getText();
            long mobileNo=Long.parseLong(mobileText.getText());
            long homeNo=Long.parseLong(HomeText.getText());

            Technician technician=new Technician(techName, techName, address, mobileNo, mobileNo);

            boolean res1=TechnicianController.addTechnician(technician);

            if(res1==true ){
                
                String repairId=repairLabel.getText();
                String itemCode=String.valueOf(ItemCodeComboBox.getSelectedItem());
                String name=NameText.getText();
                String dateOfRepairing=String.valueOf(RepairingDateCombo.getSelectedItem());
                double payment=Double.parseDouble(PriceText.getText());
                String description=DescriptionText.getText();
                String category=String.valueOf(CategoryComboBox.getSelectedItem());
                String returnDate=String.valueOf(ReturnDateCombo.getSelectedItem());

                Repair repair=new Repair(repairId, itemCode, name, techID, techName, dateOfRepairing, payment, description, category, returnDate);

                boolean res2=RepairController.addRepair(repair);

                if(res2==true){
                    JOptionPane.showMessageDialog(AddNewRepair.this,"One record added", "Information", 1);

                    infoTable1.setModel(RepairController.getAllRepairList_2()); 
                }else{
                    JOptionPane.showMessageDialog(AddNewRepair.this,"Unsuccessful", "Information", 1);
                }
            }else{
                JOptionPane.showMessageDialog(AddNewRepair.this,"Unsuccessful", "Information", 1);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddNewRepair.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HibernateException ex) {
            Logger.getLogger(AddNewRepair.class.getName()).log(Level.SEVERE, null, ex);
        }
            }else{
                try {
                    String repairId=repairLabel.getText();
                    String techID=TechnicianIDText.getText();
                    String techName=String.valueOf(TechNameComboBox.getSelectedItem());
                    String itemCode=String.valueOf(ItemCodeComboBox.getSelectedItem());
                    String name=NameText.getText();
                    String dateOfRepairing=String.valueOf(RepairingDateCombo.getSelectedItem());
                    double payment=Double.parseDouble(PriceText.getText());
                    String description=DescriptionText.getText();
                    String category=String.valueOf(CategoryComboBox.getSelectedItem());
                    String returnDate=String.valueOf(ReturnDateCombo.getSelectedItem());
                    
                    Repair repair=new Repair(repairId, itemCode, name, techID, techName, dateOfRepairing, payment, description, category, returnDate);
                    
                    boolean res2=RepairController.addRepair(repair);
                    
                    if(res2==true){
                        JOptionPane.showMessageDialog(AddNewRepair.this,"One record added", "Information", 1);
                        
                        infoTable1.setModel(RepairController.getAllRepairList_2());
                    }else{
                        JOptionPane.showMessageDialog(AddNewRepair.this,"Unsuccessful", "Information", 1);
                    }
                } catch (HibernateException ex) {
                    Logger.getLogger(AddNewRepair.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
         }else{
             JOptionPane.showMessageDialog(AddNewRepair.this,"User cancel the process", "Information", 1);
         }
    }//GEN-LAST:event_jButton1ActionPerformed

