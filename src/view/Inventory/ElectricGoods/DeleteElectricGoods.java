    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
           int respond= JOptionPane.showConfirmDialog(DeleteElectricGoods.this,"Do you want to delete this record.? If yes click Yes", "Confimation message",0,3);

            if(respond==JOptionPane.OK_OPTION){
               try {
                   String itemCode=ItemCodeText.getText();
                   int res=ElectricGoodController.deleteElectricGood(itemCode);
                   
                   if(res>0){
                       JOptionPane.showMessageDialog(DeleteElectricGoods.this,"Selected record deleted", "Information", 1);
                       
                       dtm2.setRowCount(0);
                       infoTable2.setModel(ElectricGoodController.getAllElectricGoodsList_2());
                       
                   }else{
                       JOptionPane.showMessageDialog(DeleteElectricGoods.this,"Deleting process unsuccessful", "Information", 1);
                   }           
               } catch (HibernateException ex) {
                   Logger.getLogger(DeleteElectricGoods.class.getName()).log(Level.SEVERE, null, ex);
               }
            }else{
                JOptionPane.showMessageDialog(DeleteElectricGoods.this,"User Cancel the deleting process", "Information", 1);
            }
            
                   NameText.setText("");
                   QtyText.setText("");
                   LocationText.setText("");
                   brandText.setText("");
                   purDateText.setText("");
                   InputPowerText.setText("");
                   periodText.setText("");
                   UserText.setText("");
                   SellerNameText.setText("");
                   PriceText.setText("");
                   methodText.setText("");
    }//GEN-LAST:event_deleteButtonActionPerformed

