    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        dtm2=ElectricGoodController.getAllElectricGoodsList_2();
        String itemcode1=String.valueOf(dtm2.getValueAt(infoTable2.getSelectedRow(), 0));
        String itemcode2=ItemCodeText.getText();
      
        if(itemcode2!=null){
            try {
                ArrayList<String> itemList=ElectricGoodController.SearchItem(itemcode2);

                        NameText.setText(itemList.get(0));
                        QtyText.setText(itemList.get(2));
                        LocationText.setText(itemList.get(1));
                        brandText.setText(itemList.get(5));
                        purDateText.setText(itemList.get(7));
                        InputPowerText.setText(itemList.get(4));
                        periodText.setText(itemList.get(10));
                        UserText.setText(itemList.get(3));
                        SellerNameText.setText(itemList.get(6));
                        PriceText.setText(itemList.get(8));
                        methodText.setText(itemList.get(9));

            } catch (HibernateException ex) {
                Logger.getLogger(DeleteElectricGoods.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else if(itemcode1!=null){
            ArrayList<String> itemList=ElectricGoodController.SearchItem(itemcode1);

                        NameText.setText(itemList.get(0));
                        QtyText.setText(itemList.get(2));
                        LocationText.setText(itemList.get(1));
                        brandText.setText(itemList.get(5));
                        purDateText.setText(itemList.get(7));
                        InputPowerText.setText(itemList.get(4));
                        periodText.setText(itemList.get(10));
                        UserText.setText(itemList.get(3));
                        SellerNameText.setText(itemList.get(6));
                        PriceText.setText(itemList.get(8));
                        methodText.setText(itemList.get(9));
        }else{
            JOptionPane.showMessageDialog(DeleteElectricGoods.this,"AnError occured", "Information", 1);
        }
        
    }//GEN-LAST:event_searchButtonActionPerformed

