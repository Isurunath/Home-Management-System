    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        int i = jTable1.getSelectedRow();
        String ID = jTable1.getValueAt(i, 5).toString();
        
        String name;          //Item name
        String catagory;      //Item catagory
        String price;         //Item price
        String address;       //Item supplier address
        String contact;       //Item supplier contact
        
        name = iname.getText();
        catagory = icatagory.getText();
        price = iprice.getText();
        address = iaddress.getText();
        contact = icontact.getText();
        
        //setting values to a EventItem object
        EventItems item = new EventItems();
        item.setId(Integer.parseInt(ID));
        item.setItemName(name);
        item.setEventCatagory(catagory);
        item.setAddress(address);
        item.setPrice(price);
        item.setContact(contact);
       
        //Update Item
        EventItemController.updateItem(item);
        displaytable();  //Refreash table 
        loadcombo();  //refreash combobox

    }//GEN-LAST:event_jButton4ActionPerformed

