    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String name;          //Item name
        String catagory;      //Item catagory
        String price;         //Item price
        String address;       //Item supplier address
        String contact;       //Item supplier contact
        
        name = iname.getText();
        catagory = icatagory.getText();
        price = iprice.getText();
        address = iaddress.getText();
        contact = icontact.getText();
        
        //setting values to a EventItem object
        EventItems item = new EventItems();
        item.setItemName(name);
        item.setEventCatagory(catagory);
        item.setAddress(address);
        item.setPrice(price);
        item.setContact(contact);
        
        DefaultTableModel d = new DefaultTableModel();
        d = (DefaultTableModel) jTable1.getModel();
        
        //Adding new Item
        EventItemController.addEventItem(d,item);
        displaytable();  //Refreash table 
        loadcombo();  //refreash combobox
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int response = JOptionPane.showConfirmDialog(null, "Do you want to continue?", "Confirm",
                                                     JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (response == JOptionPane.YES_OPTION) {
            //Delete item
            int i = jTable1.getSelectedRow();
            EventItemController.DeleteItems(jTable1.getValueAt(i, 5).toString());
            
            displaytable();  //Refreash table 
            loadcombo();  //refreash combobox
            
            //Clear textBoxes
            iname.setText("");
            icatagory.setText("");
            iprice.setText("");
            iaddress.setText("");
            icontact.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

