    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        DateEditor de = new DateEditor(estartTime,"hh:mm a");
        
        String name;           //event Name
        Date date;             //event Date
        String startTime;      //event Start Time
        String endTime;        //event End Time
        String location;       //event Location
        String NOP;            //NUmber of participants
        
        name = ename.getText();
        date = edate.getDate();
        endTime = (de.getFormat().format(eendTime.getValue()));
        startTime = (de.getFormat().format(estartTime.getValue()));
        location = elocation.getText();
        NOP = eNOP.getValue().toString();
        
        EventPlan newEvent = new EventPlan();
        newEvent.setEventId(updateID);
    	newEvent.setName(name);
    	newEvent.setDate(date);
    	newEvent.setStartTime(startTime);
    	newEvent.setEndTime(endTime);
    	newEvent.setLocation(location);
    	newEvent.setNop(Integer.parseInt(NOP));
    	
        //update Event main details
        EventController.updateEvent(newEvent);
        
        //update Event contact details
        Eventncontact eventncontact = new Eventncontact();
        eventncontact.setEventId(updateID);
        EventController.deleteParticipantTable(updateID);
        for(int i=0;i<participanttable.getRowCount();i++){
            eventncontact.setName(participanttable.getValueAt(i, 0).toString());
            eventncontact.setAddress(participanttable.getValueAt(i, 1).toString());
            EventController.addParticipantTable(eventncontact);
        }
        
        //update Event Item details
        Eventnitems eventnitems = new Eventnitems();
        eventnitems.setEventId(updateID);
        EventController.deleteItemTable(updateID);
        for(int i=0;i<itemTable.getRowCount();i++){
            eventnitems.setItemName(itemTable.getValueAt(i, 0).toString());
            eventnitems.setQuantity(Integer.parseInt(itemTable.getValueAt(i, 1).toString()));
            EventController.addItemTable(eventnitems);
        }
        
        //update Event food details
        Eventnfood eventnfood = new Eventnfood();
        eventnfood.setEventId(updateID);
        EventController.deleteFoodTable(updateID);
        for(int i=0;i<mainfoodtable.getRowCount();i++){
            eventnfood.setPlate(mainfoodtable.getValueAt(i, 0).toString());
            EventController.addFoodTable(eventnfood);
        }
        
        //update Event beverage details
        Eventnbev eventnbev = new Eventnbev();
        eventnbev.setEventId(updateID);
        EventController.deleteBevTable(updateID);
        for(int i=0;i<mainbevlist.getRowCount();i++){
            eventnbev.setName(mainbevlist.getValueAt(i, 0).toString());
            eventnbev.setQuantity(Integer.parseInt(mainbevlist.getValueAt(i, 1).toString()));
            EventController.addBevTable(eventnbev);
        }
        
        //this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

