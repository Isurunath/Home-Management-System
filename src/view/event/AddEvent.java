    /**
     * load Event details when updating event
     * @param ID Event ID
     * @throws ParseException 
     */
    public static void update(String ID) throws ParseException{
        List l = EventController.update_getEvent(ID);
        SimpleDateFormat time = new SimpleDateFormat("hh:mm a");
        
        for(Object o:l){
            EventPlan items = (EventPlan)o;
            updateID = items.getEventId();
            ename.setText(items.getName());
            edate.setDate(items.getDate());
            estartTime.setValue(time .parse(items.getStartTime()));
            eendTime.setValue(time .parse(items.getEndTime()));
            elocation.setText(items.getLocation());
            eNOP.setValue(items.getNop());
        }
        
        //load particiapnt table
        participanttable.setModel(EventController.update_getContactTable(ID));
        
        //load Item table
        itemTable.setModel(EventController.update_getItemTable(ID));
        
        //load food table
        mainfoodtable.setModel(EventController.update_getFoodTable(ID));
        
        //load contact table
        mainbevlist.setModel(EventController.update_getBevTable(ID)); 
    }

